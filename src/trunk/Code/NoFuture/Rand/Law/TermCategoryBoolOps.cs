        /// <summary>
        /// Equal to
        /// </summary>
        Eq,

        /// <summary>
        /// Not equal to
        /// </summary>
        Ne,

        /// <summary>
        /// Less than
        /// </summary>
        Lt,

        /// <summary>
        /// Greater than
        /// </summary>
        Gt,

        /// <summary>
        /// Less-than-equal-to
        /// </summary>
        Le,

        /// <summary>
        /// Greater-than-equal-to
        /// </summary>
        Ge
    }
}